import React, { Component } from "react";
import Box from "./Box";
import "./App.css";

class App extends Component {
  constructor(props) {
    super(props);

    const num = this.props.allColors.length;
    const boxes = Array(num)
      .fill()
      .map(box => this.randomColor(this.props.allColors));

    this.state = { boxes };

    setInterval(() => {
      this.setState(props => {
        return {
          boxes: Array(num)
            .fill()
            .map(box => this.randomColor(this.props.allColors))
        };
      });
    }, 1000);
  }

  randomColor(colors) {
    return colors[Math.floor(Math.random() * colors.length)];
  }

  render() {
    const boxes = this.state.boxes.map((color, idx) => {
      return <Box color={color} key={idx} />;
    });
    return <div className="App">{boxes}</div>;
  }
}

